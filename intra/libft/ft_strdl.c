#include "libft.h"

int		ft_strdl(char **as)
{
	char	*t;
	int		i;

	i = 0;
	if (as)
	{
		while (as[i])
		{
			t = as[i + 1];
			free(as[i]);
			as[i] = 0;
			as[++i] = t;
		}
		free(as);
		as = NULL;
	}
	return (0);
}
